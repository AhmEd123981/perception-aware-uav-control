"""Consolidated thesis/paper analysis for the perception-aware quadrotor study.

Loads the clean controller-comparison metrics (lawnmower + circular), the
corrected Monte Carlo aggregate (if present), and the U-Net segmentation test
metrics, then produces publication-quality figures, a combined master metrics
table, LaTeX tables, and a written analysis section.

Run:
    python analysis/thesis_analysis.py

All outputs are written to results/analysis/.
"""

from __future__ import annotations

import json
from pathlib import Path

import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt
import numpy as np
import pandas as pd

ROOT = Path(__file__).resolve().parents[1]
OUT = ROOT / "results" / "analysis"
OUT.mkdir(parents=True, exist_ok=True)

CONTROLLER_ORDER = ["PID", "LQR", "Hinf", "MPC", "SMC"]
CONTROLLER_LABEL = {"PID": "PID", "LQR": "LQR", "Hinf": "H∞", "MPC": "MPC", "SMC": "SMC"}
CLASS_NAMES = ["background", "drydown", "weed_cluster", "double_plant"]
AFFECTED = [False, True, True, True]

LAWN_CSV = ROOT / "results/perception_logs/day6_controller_comparison/day6_controller_metrics.csv"
CIRC_CSV = ROOT / "results/perception_logs/day6_circular/day6_controller_metrics.csv"
MC_CSV = ROOT / "results/perception_logs/monte_carlo/perception_monte_carlo_aggregate.csv"
SEG_JSON = ROOT / "deep_learning/weights/test_metrics_2020.json"  # real Agriculture-Vision av model

plt.rcParams.update({
    "font.size": 11,
    "axes.grid": True,
    "grid.alpha": 0.3,
    "axes.axisbelow": True,
    "figure.dpi": 150,
    "savefig.bbox": "tight",
})


# --------------------------------------------------------------------------- #
# Loading
# --------------------------------------------------------------------------- #
def load_controller_csv(path: Path, trajectory: str) -> pd.DataFrame:
    df = pd.read_csv(path)
    df["trajectory"] = trajectory
    df["controller"] = pd.Categorical(df["controller"], CONTROLLER_ORDER, ordered=True)
    return df.sort_values("controller").reset_index(drop=True)


def load_all() -> dict:
    data = {}
    data["lawn"] = load_controller_csv(LAWN_CSV, "lawnmower") if LAWN_CSV.exists() else None
    data["circ"] = load_controller_csv(CIRC_CSV, "circular") if CIRC_CSV.exists() else None
    data["seg"] = json.loads(SEG_JSON.read_text()) if SEG_JSON.exists() else None
    if MC_CSV.exists():
        mc = pd.read_csv(MC_CSV)
        # Treat the Monte Carlo run as "usable" only if it is not the degraded
        # Duration=12 cache (recall == 0 everywhere on the agricultural runs).
        ag = mc[mc["trajectory"] == "agricultural"]
        mc_usable = ag["affected_zone_recall_mean"].fillna(0).gt(0).any() or \
            ag["coverage_percent_mean"].max() > 60
        data["mc"] = mc
        data["mc_usable"] = bool(mc_usable)
    else:
        data["mc"] = None
        data["mc_usable"] = False
    return data


# --------------------------------------------------------------------------- #
# Tables
# --------------------------------------------------------------------------- #
def build_master_table(data: dict) -> pd.DataFrame:
    frames = [d for d in (data["lawn"], data["circ"]) if d is not None]
    master = pd.concat(frames, ignore_index=True)
    cols = ["trajectory", "controller", "coverage_percent", "affected_zone_recall",
            "false_alarm_rate_per_m2", "mean_latency_s", "median_latency_s",
            "max_latency_s", "missed_zones", "mean_inference_time_ms", "p95_inference_time_ms"]
    cols = [c for c in cols if c in master.columns]
    master = master[cols]
    master.to_csv(OUT / "master_metrics_combined.csv", index=False)
    return master


def write_latex_tables(data: dict, master: pd.DataFrame) -> None:
    lines = ["% Auto-generated by analysis/thesis_analysis.py", ""]

    def block(df: pd.DataFrame, traj: str, caption: str, label: str) -> list[str]:
        sub = df[df["trajectory"] == traj]
        rows = []
        rows.append("\\begin{table}[t]")
        rows.append("\\centering")
        rows.append(f"\\caption{{{caption}}}")
        rows.append(f"\\label{{{label}}}")
        rows.append("\\begin{tabular}{lrrrrr}")
        rows.append("\\hline")
        rows.append("Controller & Coverage [\\%] & Recall & False alarm [m$^{-2}$] & "
                    "Mean latency [s] & Inference [ms] \\\\")
        rows.append("\\hline")
        for _, r in sub.iterrows():
            rows.append(
                f"{CONTROLLER_LABEL.get(str(r['controller']), r['controller'])} & "
                f"{r['coverage_percent']:.2f} & {r['affected_zone_recall']:.2f} & "
                f"{r['false_alarm_rate_per_m2']:.3f} & {r['mean_latency_s']:.2f} & "
                f"{r['mean_inference_time_ms']:.1f} \\\\")
        rows.append("\\hline")
        rows.append("\\end{tabular}")
        rows.append("\\end{table}")
        rows.append("")
        return rows

    lines += block(master, "lawnmower",
                   "Controller performance on the agricultural lawnmower survey (single deterministic run).",
                   "tab:lawnmower")
    lines += block(master, "circular",
                   "Controller performance on the circular stress trajectory (single deterministic run).",
                   "tab:circular")

    # Segmentation table
    if data["seg"] is not None:
        t = data["seg"]["test"]
        lines.append("\\begin{table}[t]")
        lines.append("\\centering")
        lines.append("\\caption{U-Net semantic segmentation accuracy on the held-out test split "
                     f"(mean IoU = {t['mean_iou']:.3f}).}}")
        lines.append("\\label{tab:segmentation}")
        lines.append("\\begin{tabular}{lrrrc}")
        lines.append("\\hline")
        lines.append("Class & IoU & Precision & Recall & Affected \\\\")
        lines.append("\\hline")
        for i, name in enumerate(CLASS_NAMES):
            lines.append(
                f"{name.replace('_', '\\_')} & {t['per_class_iou'][i]:.3f} & "
                f"{t['per_class_precision'][i]:.3f} & {t['per_class_recall'][i]:.3f} & "
                f"{'yes' if AFFECTED[i] else 'no'} \\\\")
        lines.append("\\hline")
        lines.append("\\end{tabular}")
        lines.append("\\end{table}")
        lines.append("")

    # Monte Carlo table
    if data["mc"] is not None and data["mc_usable"]:
        mc = data["mc"]
        lines.append("\\begin{table}[t]")
        lines.append("\\centering")
        lines.append("\\caption{Monte Carlo controller statistics (mean $\\pm$ 95\\% CI over "
                     f"{int(mc['n'].iloc[0])} seeds).}}")
        lines.append("\\label{tab:montecarlo}")
        lines.append("\\begin{tabular}{llrr}")
        lines.append("\\hline")
        lines.append("Trajectory & Controller & Coverage [\\%] & Recall \\\\")
        lines.append("\\hline")
        for _, r in mc.iterrows():
            lines.append(
                f"{r['trajectory']} & {CONTROLLER_LABEL.get(r['controller'], r['controller'])} & "
                f"{r['coverage_percent_mean']:.2f} $\\pm$ {r['coverage_percent_ci95']:.2f} & "
                f"{r['affected_zone_recall_mean']:.2f} $\\pm$ {r['affected_zone_recall_ci95']:.2f} \\\\")
        lines.append("\\hline")
        lines.append("\\end{tabular}")
        lines.append("\\end{table}")
        lines.append("")

    (OUT / "thesis_tables.tex").write_text("\n".join(lines), encoding="utf-8")


