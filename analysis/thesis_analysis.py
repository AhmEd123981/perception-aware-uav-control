def write_analysis_md(data: dict, master: pd.DataFrame) -> None:
    lawn = data["lawn"].set_index("controller")
    circ = data["circ"].set_index("controller")
    seg = data["seg"]["test"] if data["seg"] else None

    def best(df, col, want_max=True):
        s = df[col]
        return (s.idxmax() if want_max else s.idxmin()), (s.max() if want_max else s.min())

    cov_best, cov_v = best(lawn, "coverage_percent", True)
    lat_best, lat_v = best(lawn, "mean_latency_s", False)

    md = []
    md.append("# Results and Analysis: Perception-Aware Controller Comparison\n")
    md.append("*Auto-generated by `analysis/thesis_analysis.py`. Figures and tables in "
              "`results/analysis/`.*\n")

    md.append("## 1. Segmentation accuracy (real Agriculture-Vision model)\n")
    if seg:
        pci = seg["per_class_iou"]; pcr = seg["per_class_recall"]
        md.append(f"The U-Net (ResNet-18 encoder) is trained and evaluated on the real "
                  f"**Agriculture-Vision CVPR-2020** dataset (4 classes: background, drydown, "
                  f"weed_cluster, double_plant). On the held-out test split it reaches a mean IoU "
                  f"of **{seg['mean_iou']:.3f}** (per-class IoU: background {pci[0]:.3f}, drydown "
                  f"{pci[1]:.3f}, weed_cluster {pci[2]:.3f}, double_plant {pci[3]:.3f}). "
                  f"Affected-class recall — the property that matters for a spraying task, where a "
                  f"missed patch is costlier than a false alarm — is drydown {pcr[1]:.3f}, "
                  f"weed_cluster {pcr[2]:.3f}, double_plant {pcr[3]:.3f} "
                  f"(Fig. `segmentation_per_class.png`, `segmentation_confusion.png`). These are "
                  f"**real-imagery** numbers and are the honest accuracy claim for this thesis. "
                  f"They are far below the ~0.95 mIoU of the earlier synthetic generator, which is "
                  f"trained and tested on the same procedural process and is therefore retained "
                  f"only as a labelled pipeline sanity benchmark, never as an accuracy claim.\n")

    md.append("## 2. Controller comparison — lawnmower survey\n")
    md.append("On the nominal agricultural lawnmower trajectory the controllers separate clearly "
              "(Table `tab:lawnmower`, Fig. `controller_comparison_panel.png`):\n")
    md.append(f"- **Coverage:** {CONTROLLER_LABEL.get(str(cov_best))} leads at {cov_v:.1f}%; "
              f"PID ({lawn.loc['PID','coverage_percent']:.1f}%) and SMC "
              f"({lawn.loc['SMC','coverage_percent']:.1f}%) track the reference tightly enough to "
              f"sweep nearly the whole field, while LQR ({lawn.loc['LQR','coverage_percent']:.1f}%) "
              f"and H∞ ({lawn.loc['Hinf','coverage_percent']:.1f}%) leave large gaps.\n")
    md.append(f"- **Affected-zone recall:** PID, MPC and SMC detect every affected zone "
              f"(recall 1.00); H∞ reaches {lawn.loc['Hinf','affected_zone_recall']:.2f} and "
              f"LQR only {lawn.loc['LQR','affected_zone_recall']:.2f}. Recall tracks coverage: a "
              f"controller that cannot follow the survey lines simply never images some patches.\n")
    md.append(f"- **Detection-to-treatment latency:** {CONTROLLER_LABEL.get(str(lat_best))} is "
              f"fastest at {lat_v:.1f} s mean; LQR is slowest "
              f"({lawn.loc['LQR','mean_latency_s']:.1f} s). Lower coverage pushes both recall and "
              f"latency the wrong way.\n")
    md.append(f"- **False alarms are coupled to coverage:** the per-m$^2$ false-alarm rate is "
              f"highest for the high-coverage controllers (PID "
              f"{lawn.loc['PID','false_alarm_rate_per_m2']:.2f}, SMC "
              f"{lawn.loc['SMC','false_alarm_rate_per_m2']:.2f}) and lowest for low-coverage LQR "
              f"({lawn.loc['LQR','false_alarm_rate_per_m2']:.2f}) — a UAV that images more of the "
              f"field also accumulates more false-positive affected cells. This metric must be read "
              f"jointly with coverage, not as an independent quality score.\n")

    md.append("## 3. Controller comparison — circular stress trajectory\n")
    md.append("The circular reference is an aggressive stress test scored against the same real "
              "ground truth. Every controller still detects the affected zones (recall "
              f"{circ['affected_zone_recall'].min():.2f}–{circ['affected_zone_recall'].max():.2f}), "
              "so on this trajectory the discriminator is **coverage** and **false-alarm rate**, "
              f"not recall: MPC and PID sweep the most field ({circ.loc['MPC','coverage_percent']:.1f}/"
              f"{circ.loc['PID','coverage_percent']:.1f}%), while LQR and H∞ drop to "
              f"{circ.loc['LQR','coverage_percent']:.1f}/{circ.loc['Hinf','coverage_percent']:.1f}% "
              f"coverage and carry the highest false-alarm rates "
              f"({circ.loc['LQR','false_alarm_rate_per_m2']:.2f}/"
              f"{circ.loc['Hinf','false_alarm_rate_per_m2']:.2f} m$^{{-2}}$). Note that PID and SMC "
              "leave several detected zones unsprayed within the flight/replan budget on this path, "
              "producing an undefined (NaN) mean treatment latency — a genuine limitation of those "
              "controllers on the circular trajectory rather than a metric error.\n")

    md.append("## 4. Perception runtime\n")
    md.append("Per-frame ONNX inference runs at "
              f"{lawn['mean_inference_time_ms'].min():.0f}–"
              f"{lawn['mean_inference_time_ms'].max():.0f} ms on CPU "
              "(Fig. `perception_runtime.png`). MPC and SMC carry the heaviest control compute, "
              "which shows up as longer tails (higher p95). All controllers stay far below the "
              "0.5–5 Hz camera budget, so perception is not the bottleneck at survey speed.\n")

    if data["mc_usable"]:
        md.append("## 5. Statistical robustness (Monte Carlo)\n")
        mc = data["mc"]
        n = int(mc["n"].iloc[0])
        md.append(f"Across **{n} seeds** with randomized wind, initial conditions, and camera "
                  "noise, the controller ranking is stable; mean±95% CI bars do not overlap "
                  "between the strong (PID/SMC/MPC) and weak (LQR/H∞) groups on coverage "
                  "(Table `tab:montecarlo`, Fig. `monte_carlo_panel.png`).\n")
    else:
        md.append("## 5. Statistical robustness (Monte Carlo)\n")
        md.append("> **Pending.** No Monte Carlo aggregate from the fixed real-ground-truth "
                  "pipeline is present yet (the earlier synthetic-field aggregate has been set "
                  "aside so it is not reported as if it were the real result). Generate a real-GT "
                  "Monte Carlo table with:\n\n"
                  "```\nmatlab -batch \"addpath('scripts'); run_thesis_rerun(10)\"\n```\n\n"
                  "This runs the 5 Hz real-model pipeline over 10 seeds and writes "
                  "`results/perception_logs/monte_carlo/perception_monte_carlo_aggregate.csv`, "
                  "after which re-running this script fills in Table `tab:montecarlo`.\n")

    md.append("## 6. Takeaways for the thesis / paper\n")
    md.append("1. **PID and SMC are the strongest agricultural-survey controllers** on both "
              "coverage and recall, while MPC delivers competitive coverage at the lowest latency.\n")
    md.append("2. **LQR and H∞ under-perform** on tracking-driven coverage, and the deficit "
              "persists on the circular trajectory.\n")
    iou_txt = f"{seg['mean_iou']:.3f}" if seg else "n/a"
    md.append(f"3. **Perception accuracy is real but modest** (held-out test mIoU {iou_txt} on "
              "Agriculture-Vision) and cheap (<0.12 s/frame on CPU), so the joint-autonomy "
              "bottleneck is a mix of flight-control tracking and the sim-to-field perception gap, "
              "not inference cost.\n")
    md.append("4. Accuracy is reported on **real imagery**; the earlier synthetic mIoU≈0.95 is "
              "explicitly excluded from every headline claim and kept only as a sanity benchmark.\n")

    (OUT / "ANALYSIS.md").write_text("\n".join(md), encoding="utf-8")


# --------------------------------------------------------------------------- #
def main() -> None:
    data = load_all()
    if data["lawn"] is None or data["circ"] is None:
        raise SystemExit("Missing day6 controller metric CSVs; cannot build analysis.")
    master = build_master_table(data)
    write_latex_tables(data, master)
    fig_controller_panel(data)
    fig_runtime(data)
    fig_segmentation(data)
    fig_confusion(data)
    fig_monte_carlo(data)
    write_analysis_md(data, master)

    print("Analysis complete. Outputs in", OUT)
    print("Monte Carlo usable:", data["mc_usable"])
    for p in sorted(OUT.glob("*")):
        print("  ", p.relative_to(ROOT))


if __name__ == "__main__":
    main()
