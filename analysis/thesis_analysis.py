def fig_controller_panel(data: dict) -> None:
    """2x2 grouped bars: coverage, recall, false alarm, mean latency, both trajectories."""
    metrics = [
        ("coverage_percent", "Coverage [%]"),
        ("affected_zone_recall", "Affected-zone recall"),
        ("false_alarm_rate_per_m2", "False-alarm rate [1/m$^2$]"),
        ("mean_latency_s", "Mean detection-to-treatment latency [s]"),
    ]
    x = np.arange(len(CONTROLLER_ORDER))
    w = 0.38
    fig, axes = plt.subplots(2, 2, figsize=(11, 8))
    for ax, (col, title) in zip(axes.ravel(), metrics):
        lawn = data["lawn"].set_index("controller")[col].reindex(CONTROLLER_ORDER).values
        circ = data["circ"].set_index("controller")[col].reindex(CONTROLLER_ORDER).values
        ax.bar(x - w / 2, lawn, w, label="lawnmower", color="#2c7fb8")
        ax.bar(x + w / 2, circ, w, label="circular", color="#d95f0e")
        ax.set_title(title)
        ax.set_xticks(x)
        ax.set_xticklabels([CONTROLLER_LABEL[c] for c in CONTROLLER_ORDER])
        ax.legend(fontsize=9)
    fig.suptitle("Controller comparison across trajectories (single deterministic run)", fontsize=13)
    fig.tight_layout(rect=[0, 0, 1, 0.97])
    fig.savefig(OUT / "controller_comparison_panel.png")
    plt.close(fig)


def fig_runtime(data: dict) -> None:
    x = np.arange(len(CONTROLLER_ORDER))
    w = 0.38
    fig, ax = plt.subplots(figsize=(8, 4.5))
    for off, key, lbl, col in [(-w / 2, "lawn", "lawnmower", "#2c7fb8"),
                               (w / 2, "circ", "circular", "#d95f0e")]:
        d = data[key].set_index("controller").reindex(CONTROLLER_ORDER)
        mean = d["mean_inference_time_ms"].values
        p95 = d["p95_inference_time_ms"].values
        ax.bar(x + off, mean, w, label=f"{lbl} mean", color=col)
        ax.errorbar(x + off, mean, yerr=np.clip(p95 - mean, 0, None),
                    fmt="none", ecolor="black", capsize=3, lw=1,
                    label=f"{lbl} p95" if off < 0 else None)
    ax.set_xticks(x)
    ax.set_xticklabels([CONTROLLER_LABEL[c] for c in CONTROLLER_ORDER])
    ax.set_ylabel("ONNX inference time [ms]")
    ax.set_title("Per-frame perception runtime (mean, whisker to p95)")
    ax.legend(fontsize=9)
    fig.tight_layout()
    fig.savefig(OUT / "perception_runtime.png")
    plt.close(fig)


def fig_segmentation(data: dict) -> None:
    if data["seg"] is None:
        return
    t = data["seg"]["test"]
    x = np.arange(len(CLASS_NAMES))
    w = 0.27
    fig, ax = plt.subplots(figsize=(9, 4.5))
    ax.bar(x - w, t["per_class_iou"], w, label="IoU", color="#1b9e77")
    ax.bar(x, t["per_class_precision"], w, label="Precision", color="#7570b3")
    ax.bar(x + w, t["per_class_recall"], w, label="Recall", color="#d95f02")
    ax.set_xticks(x)
    ax.set_xticklabels([c.replace("_", "\n") for c in CLASS_NAMES])
    ax.set_ylim(0.0, 1.0)  # real av per-class IoU spans 0.18-0.81, not the 0.9-1.0 synthetic range
    ax.set_ylabel("Score")
    ax.set_title(f"U-Net per-class test accuracy (mean IoU = {t['mean_iou']:.3f})")
    ax.legend(fontsize=9, loc="lower right")
    fig.tight_layout()
    fig.savefig(OUT / "segmentation_per_class.png")
    plt.close(fig)


def fig_confusion(data: dict) -> None:
    if data["seg"] is None:
        return
    cm = np.array(data["seg"]["test"]["confusion_matrix"], dtype=float)
    cmn = cm / cm.sum(axis=1, keepdims=True)
    fig, ax = plt.subplots(figsize=(6, 5))
    im = ax.imshow(cmn, cmap="Blues", vmin=0, vmax=1)
    n = len(CLASS_NAMES)
    ax.set_xticks(range(n)); ax.set_yticks(range(n))
    ax.set_xticklabels(CLASS_NAMES, rotation=45, ha="right", fontsize=8)
    ax.set_yticklabels(CLASS_NAMES, fontsize=8)
    ax.set_xlabel("Predicted"); ax.set_ylabel("True")
    ax.set_title("Row-normalized confusion matrix (test)")
    for i in range(n):
        for j in range(n):
            ax.text(j, i, f"{cmn[i, j]:.2f}", ha="center", va="center",
                    color="white" if cmn[i, j] > 0.5 else "black", fontsize=8)
    fig.colorbar(im, fraction=0.046, pad=0.04)
    fig.tight_layout()
    fig.savefig(OUT / "segmentation_confusion.png")
    plt.close(fig)


def fig_monte_carlo(data: dict) -> None:
    if data["mc"] is None or not data["mc_usable"]:
        return
    mc = data["mc"]
    metrics = [
        ("coverage_percent", "Coverage [%]"),
        ("affected_zone_recall", "Affected-zone recall"),
        ("mean_latency_s", "Mean latency [s]"),
        ("false_alarm_rate_per_m2", "False-alarm rate [1/m$^2$]"),
    ]
    trajs = ["agricultural", "circular"]
    x = np.arange(len(CONTROLLER_ORDER))
    w = 0.38
    fig, axes = plt.subplots(2, 2, figsize=(11, 8))
    for ax, (base, title) in zip(axes.ravel(), metrics):
        for off, traj, col in [(-w / 2, "agricultural", "#2c7fb8"), (w / 2, "circular", "#d95f0e")]:
            sub = mc[mc["trajectory"] == traj].set_index("controller").reindex(CONTROLLER_ORDER)
            mean = sub[f"{base}_mean"].values
            ci = sub[f"{base}_ci95"].values
            ax.bar(x + off, mean, w, label=traj, color=col)
            ax.errorbar(x + off, mean, yerr=ci, fmt="none", ecolor="black", capsize=3, lw=1)
        ax.set_title(title)
        ax.set_xticks(x)
        ax.set_xticklabels([CONTROLLER_LABEL[c] for c in CONTROLLER_ORDER])
        ax.legend(fontsize=9)
    fig.suptitle(f"Monte Carlo controller statistics (n={int(mc['n'].iloc[0])} seeds, 95% CI)", fontsize=13)
    fig.tight_layout(rect=[0, 0, 1, 0.97])
    fig.savefig(OUT / "monte_carlo_panel.png")
    plt.close(fig)


